#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	while ((*s1 || *s2) && n > 0)
	{
		if (*s1 == *s2)
		{
			s1++;
			s2++;
		}
		if (*s1 != *s2)
			return (0);
		if (*s1 == '\0' && *s2 == '\0')
			return (1);
		n--;
	}
	return (0);
}
