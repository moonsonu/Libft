#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*rc;
	int		i;
	int		l;

	i = 0;
	l = ft_strlen(rc);
	rc = (char*)malloc(sizeof(char) * l);
	while (s1[i] != '\0')
	{
		rc[i] = s1[i];
		i++;
	}
	rc[i] = '\0';
	return (rc);
}
