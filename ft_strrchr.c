#include "libft.h"

char	*ft_strrchr(const char *s1, int c)
{
	char	*s1_1;
	int		i;

	s1_1 = (char *)s1;
	i = ft_strlen(s1_1) - 1;
	while (s1_1[i] != '\0')
	{
		if (s1_1[i] == c)
			return (&s1_1[i]);
		i--;
	}
	return (NULL);
}
