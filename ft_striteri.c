#include "libft.h"

void		ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	a;

	a = 0;
	if (*s || *f)
	{
		while (*s)
		{
			f(a, s);
			a++;
			s++;
		}
	}
}
